/* Mantıksal Operatörler
&& :ve
|| :veya
! :değil
*/

let yas = 20;
let para = 3500;
let saglik = true;

console.log(yas >= 18 && para >= 3000 && saglik); // true, çünkü tüm koşullar sağlanıyor.
console.log(yas >= 18 || para >= 3000 || saglik); // true, çünkü en az bir koşul sağlanıyor.

